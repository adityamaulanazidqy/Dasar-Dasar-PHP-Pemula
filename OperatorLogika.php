<?php

/* Operator Logika

    $a && $b    True Jika Keduanya True
    $a || $b    True Jika Salah Satu Atau Keduanya True
    !$a         True Jika Nalai $a False
    $a xor $b   True Jika Salah Satunya True
*/

// Contoh

var_dump(true && true);
var_dump(true && false);
var_dump(true || false);
var_dump(true xor true);
var_dump(!true);
