    $a == $b    | Sama Dengan
    $a === $b   | Identik
    $a != $b    | Tidak Sama Dengan
    $a !==$b    | Tidak Identik
    $a <> $b    | Tidak Sama Dengan
    DLL.
*/

// contoh

